    public static String renderData(String path) {

        String str = "";

        try {
            FileReader fileReader = new FileReader(new File(path));

            int check_ = fileReader.read();

            while (check_ != -1) {

                str += (char) check_;

                check_ = fileReader.read();
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(panelThemMoiThongTin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(panelThemMoiThongTin.class.getName()).log(Level.SEVERE, null, ex);
        }

        return str;

    }

