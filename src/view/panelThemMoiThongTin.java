    private void btnThem_4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem_4ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");

        ThongTin_ChuyenDeDeTai objChuyenDe = new ThongTin_ChuyenDeDeTai();

        objChuyenDe.setMaChuyenDe(txtMaChuyenDe.getText().trim());

        objChuyenDe.setTenChuyenDe(txtTenChuyenDe.getText().trim());

        objChuyenDe.setMaDeTai(txtMaDeTai_3.getText().trim());

        objChuyenDe.setSoTien(Double.parseDouble(txtSoTien.getText().trim()));

        objChuyenDe.setThoiGianBatDau(dateFormat.format(jDateBatDau1.getDate()));

        objChuyenDe.setThoiGianKetThuc(dateFormat.format(jDateKetThuc1.getDate()));

        objChuyenDe.setNgayKiHopDong(dateFormat.format(jDateNgayKyHopDong.getDate()));

        objChuyenDe.setNgayThanhLyHopDong(dateFormat.format(jDateNgayThanhLyHopDong.getDate()));

        objChuyenDe.setNguoiThucHien(txtNguoiThucHien.getText().trim());

        objChuyenDe.setSoHopDong(txtSoHopDong.getText().trim());

        objChuyenDe.setBaoCaoChuyenDe(renderData(txtBaoCaoChuyenDe.getText().trim()));

        objChuyenDe.setHopDongVaThanhLy(renderData(txtHopDongThanhLy.getText().trim()));

        if (jRChuaThanhToan.isSelected()) {
            objChuyenDe.setTinhTrangThucHien("Chưa thanh toán");
        } else {
            objChuyenDe.setTinhTrangThucHien("Đã thanh toán");
        }

        objChuyenDe.setSoTienThue(Double.parseDouble(txtSoThue.getText().trim()));

        if (objChuyenDe.getMaChuyenDe().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Mã chuyên đề đề tài trống !");

            return;
        } else {

            try {
                utils.Utils.themChuyenDeDeTaiVaoSQL(objChuyenDe);

                JOptionPane.showMessageDialog(null, "Thêm mới thành công");

                clearFormChuyenDe();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Có lỗi khi thêm mới");
                System.out.println(ex.toString());
            }
        }

    }//GEN-LAST:event_btnThem_4ActionPerformed

